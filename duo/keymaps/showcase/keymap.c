// 初期化関係

void keyboard_post_init_user(void) {
    rgblight_layers = my_rgb_layers;
    pointing_device_set_cpi(1800);
    set_auto_mouse_enable(true);
    rgblight_enable();
    rgblight_sethsv(HSV_PURPLE);
    rgblight_mode(RGBLIGHT_MODE_STATIC_LIGHT);

}

bool led_update_user(led_t led_state) {
    rgblight_set_layer_state(0, led_state.caps_lock);
    rgblight_set_layer_state(1, led_state.num_lock);
    rgblight_set_layer_state(2, led_state.scroll_lock);
    return true;
}

// カーソル調整
report_mouse_t pointing_device_task_user(report_mouse_t mouse_report) {
    double rad = 80 * (M_PI / 180) * -1;
    int8_t x_rev =  + mouse_report.x * cos(rad) - mouse_report.y * sin(rad);
    int8_t y_rev =  + mouse_report.x * sin(rad) + mouse_report.y * cos(rad);

    mouse_report.x = -1 * x_rev;
    mouse_report.y = y_rev;
    return mouse_report;
    }

// レイヤー関係

layer_state_t layer_state_set_user(layer_state_t state) {
    state = update_tri_layer_state(state, 1, 2, 3);
    rgblight_set_layer_state(3, layer_state_cmp(state, ONOFF));
    rgblight_set_layer_state(4, layer_state_cmp(state, OFFON));
    rgblight_set_layer_state(5, layer_state_cmp(state, ONON));
    rgblight_set_layer_state(6, layer_state_cmp(state, LOWER));
    rgblight_set_layer_state(7, layer_state_cmp(state, UPPER));
    rgblight_set_layer_state(8, layer_state_cmp(state, MOUSE));
    rgblight_set_layer_state(9, layer_state_cmp(state, UTIL));
    rgblight_set_layer_state(10, layer_state_cmp(state, BALL_SETTINGS));
    rgblight_set_layer_state(11, layer_state_cmp(state, LIGHT_SETTINGS));
    return state;
}


