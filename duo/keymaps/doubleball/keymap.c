// 初期化関係
void matrix_init_user(void) {
    gp27_newt = analogReadPin(GP27);
    gp28_newt = analogReadPin(GP28);
    key_up.row = 3;
    key_up.col = 6;
    key_down.row = 1;
    key_down.col = 6;
    key_left.row = 2;
    key_left.col = 6;
    key_right.row = 4;
    key_right.col = 6;
    if(gp27_newt < NO_JOYSTICK_VAL ||  gp28_newt < NO_JOYSTICK_VAL ){
        joystick_attached = false;
    }else{
        joystick_attached = true;
    }
    scrolling = false;
    scroll_accumulated_l_h = 0;
    scroll_accumulated_l_v = 0;
    scroll_accumulated_r_h = 0;
    scroll_accumulated_r_v = 0;
    pre_layer = 0;
    cur_layer = 0;
    oled_mode = true;
}

void keyboard_post_init_user(void) {
    ballconfig.raw = eeconfig_read_user();
    pointing_device_set_cpi_on_side(true, cpi_array[ballconfig.cpi_idx]);
    pointing_device_set_cpi_on_side(false, cpi_array[ballconfig.cpi_idx]);
    set_auto_mouse_enable(ballconfig.auto_mouse);
}


void eeconfig_init_user(void) {
    ballconfig.cpi_idx = CPI_DEFAULT;
    ballconfig.left_angle_idx = ANGLE_DEFAULT_LEFT;
    ballconfig.right_angle_idx = ANGLE_DEFAULT_RIGHT;
    ballconfig.left_inv = false;
    ballconfig.left_scmode = true;
    ballconfig.right_inv = false;
    ballconfig.right_scmode = false;
    ballconfig.inv_sc = false;
    ballconfig.auto_mouse = true;
    eeconfig_update_user(ballconfig.raw);
}

report_mouse_t pointing_device_task_combined_user(report_mouse_t left_report, report_mouse_t right_report) {

    double l_rad = angle_array[ballconfig.left_angle_idx] * (M_PI / 180) * -1;
    int8_t l_x_rev =  + left_report.x * cos(l_rad) - left_report.y * sin(l_rad);
    int8_t l_y_rev =  + left_report.x * sin(l_rad) + left_report.y * cos(l_rad);
    if(ballconfig.left_inv){
            l_x_rev = -1 * l_x_rev;
    }
    // 左手
    if(scrolling || ballconfig.left_scmode){
        if (abs(l_x_rev) > abs(l_y_rev)) {
            l_y_rev = 0;
        } else {
            l_x_rev = 0;
        }
        left_report.h = l_x_rev;
        left_report.v = l_y_rev;
        if(!ballconfig.inv_sc ){
            left_report.h = -1 * left_report.h;
            left_report.v = -1 * left_report.v;
        }
        scroll_accumulated_l_h += (float)left_report.h / SCROLL_DIVISOR;
        scroll_accumulated_l_v += (float)left_report.v / SCROLL_DIVISOR;
        left_report.h = (int8_t)scroll_accumulated_l_h;
        left_report.v = (int8_t)scroll_accumulated_l_v;
        scroll_accumulated_l_h -= (int8_t)scroll_accumulated_l_h;
        scroll_accumulated_l_v -= (int8_t)scroll_accumulated_l_v;
        left_report.x = 0;
        left_report.y = 0;
    }else{
        left_report.x = -1 * l_x_rev;
        left_report.y = l_y_rev;
    }

    // 右手
    double r_rad = angle_array[ballconfig.right_angle_idx] * (M_PI / 180) * -1;
    int8_t r_x_rev =  + right_report.x * cos(r_rad) - right_report.y * sin(r_rad);
    int8_t r_y_rev =  + right_report.x * sin(r_rad) + right_report.y * cos(r_rad);
    if(ballconfig.right_inv){
            r_x_rev = -1 * r_x_rev;
    }
    if(scrolling || ballconfig.right_scmode){
        if (abs(r_x_rev) > abs(r_y_rev)) {
            r_y_rev = 0;
        } else {
            r_x_rev = 0;
        }
        right_report.h = r_x_rev;
        right_report.v = r_y_rev;
        if(!ballconfig.inv_sc ){
            right_report.h = -1 * right_report.h;
            right_report.v = -1 * right_report.v;
        }
        scroll_accumulated_r_h += (float)right_report.h / SCROLL_DIVISOR;
        scroll_accumulated_r_v += (float)right_report.v / SCROLL_DIVISOR;
        right_report.h = (int8_t)scroll_accumulated_r_h;
        right_report.v = (int8_t)scroll_accumulated_r_v;
        scroll_accumulated_r_h -= (int8_t)scroll_accumulated_r_h;
        scroll_accumulated_r_v -= (int8_t)scroll_accumulated_r_v;
        right_report.x = 0;
        right_report.y = 0;
    }else{
        right_report.x = -1 * r_x_rev;
        right_report.y = r_y_rev;
    }

    return pointing_device_combine_reports(left_report, right_report);
}

// キースキャン関係
bool process_record_user(uint16_t keycode, keyrecord_t* record) {
    if (keycode == CPI_I && record->event.pressed) {
        ballconfig.cpi_idx = ballconfig.cpi_idx + 1;
        if(ballconfig.cpi_idx >= CPI_OPTION_SIZE){
            ballconfig.cpi_idx = CPI_OPTION_SIZE-1;
        }
        eeconfig_update_user(ballconfig.raw);

        pointing_device_set_cpi_on_side(true, cpi_array[ballconfig.cpi_idx]);
        pointing_device_set_cpi_on_side(false, cpi_array[ballconfig.cpi_idx]);
    }
    if (keycode == CPI_D && record->event.pressed) {
        if(ballconfig.cpi_idx > 0){
            ballconfig.cpi_idx = ballconfig.cpi_idx - 1;
        }
        eeconfig_update_user(ballconfig.raw);
        pointing_device_set_cpi_on_side(true, cpi_array[ballconfig.cpi_idx]);
        pointing_device_set_cpi_on_side(false, cpi_array[ballconfig.cpi_idx]);
    }
    if (keycode == L_ANG_I && record->event.pressed) {
        ballconfig.left_angle_idx = (ballconfig.left_angle_idx + 1) % ANGLE_OPTION_SIZE;
        eeconfig_update_user(ballconfig.raw);
    }
    if (keycode == L_ANG_D && record->event.pressed) {
        ballconfig.left_angle_idx = (ANGLE_OPTION_SIZE + ballconfig.left_angle_idx - 1) % ANGLE_OPTION_SIZE;
        eeconfig_update_user(ballconfig.raw);
    }
    if (keycode == R_ANG_I && record->event.pressed) {
        ballconfig.right_angle_idx = (ballconfig.right_angle_idx + 1) % ANGLE_OPTION_SIZE;
        eeconfig_update_user(ballconfig.raw);
    }
    if (keycode == R_ANG_D && record->event.pressed) {
        ballconfig.right_angle_idx = (ANGLE_OPTION_SIZE + ballconfig.right_angle_idx - 1) % ANGLE_OPTION_SIZE;
        eeconfig_update_user(ballconfig.raw);
    }
    if (keycode == L_INV && record->event.pressed) {
       ballconfig.left_inv = !ballconfig.left_inv;
        eeconfig_update_user(ballconfig.raw);
    }
    if (keycode == R_INV && record->event.pressed) {
       ballconfig.right_inv = !ballconfig.right_inv;
        eeconfig_update_user(ballconfig.raw);
    }
    if (keycode == L_CHMOD && record->event.pressed) {
       ballconfig.left_scmode = !ballconfig.left_scmode;
        eeconfig_update_user(ballconfig.raw);
    }
    if (keycode == R_CHMOD && record->event.pressed) {
       ballconfig.right_scmode = !ballconfig.right_scmode;
        eeconfig_update_user(ballconfig.raw);
    }
    if (keycode == INV_SCRL && record->event.pressed) {
       ballconfig.inv_sc = !ballconfig.inv_sc;
        eeconfig_update_user(ballconfig.raw);
    }
    if (keycode == MOD_SCRL) {
        scrolling = record->event.pressed;
    }
    if (keycode == AUTO_MOUSE && record->event.pressed) {
        ballconfig.auto_mouse = !ballconfig.auto_mouse;
        set_auto_mouse_enable(ballconfig.auto_mouse);
        eeconfig_update_user(ballconfig.raw);
    }
    if (keycode == OLED_MOD && record->event.pressed) {
        oled_mode = !oled_mode;
        oled_clear();
    }
    return true;
}

void matrix_scan_user(void) {
    if(joystick_attached){
        int16_t gp27_val = analogReadPin(GP27);
        int16_t gp28_val = analogReadPin(GP28);
        float x_val = ( (float)gp27_val - (float)gp27_newt  ) * (float)511 / ((float)1023 - (float)gp27_newt);
        float y_val = ( (float)gp28_val - (float)gp28_newt ) * (float)511 / ((float)1023 - (float)gp28_newt);
        if(x_val < -511){
            x_val = -511;
        }
        if(y_val < -511){
            y_val = -511;
        }

        int8_t layer = layer_switch_get_layer(key_up);
        int16_t keycode_up = keymap_key_to_keycode(layer, key_up);
        int16_t keycode_left = keymap_key_to_keycode(layer, key_left);
        int16_t keycode_right = keymap_key_to_keycode(layer, key_right);
        int16_t keycode_down = keymap_key_to_keycode(layer, key_down);

        if(pressed_left == false && x_val > (float)OFFSET){
            register_code(keycode_left);
            pressed_left = true;
            wait_ms(100);
        }else if(pressed_left == true && x_val <(float)OFFSET){
            unregister_code(keycode_left);
            pressed_left = false;
            wait_ms(100);
        }else if(pressed_right == false && x_val < (float)-1 * (float)OFFSET){
            register_code(keycode_right);
            pressed_right = true;
            wait_ms(100);
        }else if (pressed_right == true && x_val > (float)-1 * (float)OFFSET){
            unregister_code(keycode_right);
            pressed_right = false;
            wait_ms(100);
        }

        if(pressed_up == false && y_val > (float)OFFSET){
            register_code(keycode_up);
            pressed_up = true;
            wait_ms(100);
        }else if(pressed_up == true && y_val < (float)OFFSET){
            unregister_code(keycode_up);
            pressed_up = false;
            wait_ms(100);
        }else if(pressed_down == false && y_val < (float)-1 * (float)OFFSET){
            register_code(keycode_down);
            pressed_down = true;
            wait_ms(100);
        }else if(pressed_down == true && y_val > (float)-1 * (float)OFFSET){
            unregister_code(keycode_down);
            pressed_down = false;
            wait_ms(100);
        }
    }
}



