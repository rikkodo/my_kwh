// トラックボール
#define CPI_OPTIONS {800, 1200, 1600, 2200}
#define CPI_DEFAULT 2
#define ANGLE_OPTIONS { 70, 80, 90, 160, 170, 180, 250, 260, 270, 340, 350, 360}
#define ANGLE_DEFAULT_LEFT 2
#define ANGLE_DEFAULT_RIGHT 2
#define CPI_OPTION_SIZE (sizeof(cpi_array) / sizeof(uint16_t))
#define ANGLE_OPTION_SIZE (sizeof(angle_array) / sizeof(uint16_t))
#define SCROLL_DIVISOR 150.0

typedef union{
    uint32_t raw;
    struct{
        uint8_t cpi_idx         :3;
        uint8_t left_angle_idx  :5;
        uint8_t right_angle_idx :5;
        bool left_inv           :1;
        bool left_scmode        :1;
        bool right_inv          :1;
        bool right_scmode       :1;
        bool inv_sc             :1;
        bool auto_mouse         :1;
    };
} ballconfig_t;

ballconfig_t ballconfig;
uint16_t cpi_array[] = CPI_OPTIONS;
uint16_t angle_array[] = ANGLE_OPTIONS;
bool scrolling;
float scroll_accumulated_l_h;
float scroll_accumulated_l_v;
float scroll_accumulated_r_h;
float scroll_accumulated_r_v;
uint8_t pre_layer;
uint8_t cur_layer;
bool oled_mode;

// ジョイスティック
#define OFFSET  300
#define NO_JOYSTICK_VAL 100
int16_t gp27_newt;
int16_t gp28_newt;
